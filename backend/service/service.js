exports.getSumOfTwoNumber = (num1, num2) => {
    return (num1 + num2).toFixed(2);
}

exports.getSubtractionOfTwoNumber = (num1, num2) => {
    return (num1 - num2).toFixed(2);
}

exports.getMultiplicationOfTwoNumber = (num1, num2) => {
    return (num1 * num2).toFixed(2);
}

exports.getDivisionOfTwoNumber = (num1, num2) => {
    return (num1 / num2).toFixed(2);
}